//countdown using while loop
{
    let i=10;
    {
        while(i>=1)
        {
            document.write(i +"<br>")
            i--;
        }
    }
}
//countdown using for loop
document.write("for loop")
{
    let i=10;
    {
        for(i=10;i>=1;i--)
        {
            document.write(i +"<br>") 
        }
    }
}
//countdown using do..while loop
document.write("do..while loop")
{
    let i=10;
    do{
        document.write(i +"<br>") 
        i--
    }
    while(i>=1)
}

