//print A to Z using for loop
console.log("for loop")
{
    for(let i=65;i<=90;i++)
    {
        console.log(String.fromCharCode(i));
    }
}
//print A to Z using while loop
console.log("while loop")
{
    i=65;
    while(i<=90)
    {
        console.log(String.fromCharCode(i));
        i++;
    }
}
