//print 1 to n number using for loop 
console.log("for loop") 
{ 
    n=prompt("enter n number") 
    for(i=0;i<=n;i++) 
    { 
        console.log(i) 
    } 
} 
//print 1 to n number using while loop 
console.log("while loop") 
{ 
    n=prompt("enter n number") 
    i=1; 
    while(i<=n) 
    { 
        console.log(i) 
        i++; 
    } 
} 
//print 1 to n number using do..while loop 
console.log("do.while loop") 
{ 
    n=prompt("enter n number") 
    let i=1 
    do{ 
        console.log(i); 
        i++; 
    } 
    while(i<=n) 
}
