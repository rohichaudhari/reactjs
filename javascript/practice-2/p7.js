//print odd number up to n using for loop
console.log("for loop")
{
    n=prompt("enter n number")
    for(let i=0;i<=n;i++)
    {
        i=i+1;
        console.log(i);
    }
}
//print odd number up to n using while loop
console.log("while loop")
{
    n=prompt("enter n number") 
    i=0;
    while(i<=n)
    {
        i=i+1;
        console.log(i)
        i++;
    } 
}
