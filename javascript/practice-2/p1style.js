//print 10 to 1 using for loop
console.log("for loop")
{
    for(i=10; i>=1;i--)
    {
        console.log(i)
    }
}
//print 10 to 1 using while
console.log("while loop")
{
    let i=10;
    {
        while(i>=1)
        {
            console.log(i)
            i--;
        }
    }
}
//print 10 to 1 using do.. while
console.log("do..while loop")
{
    let i=10;
    do{
        console.log(i) 
        i--
    }
    while(i>=1)
}
