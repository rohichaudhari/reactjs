//print 51 to 99 using for loop
console.log("for loop")
{
    for(i=59;i<=99;i++)
    {
        console.log(i)
    }
}
//print 51 to 99 using while loo
console.log("while loop")
{
    let i=59;
    while(i<=99){
        console.log(i)
        i++;
    }
}
//print 51 to 99 using do.. while loo
console.log("do..while loop")
{
    let i=59;
    do{
        console.log(i)
        i++;
    }
    while(i<=99)
}
